public class NumerosPares {
    public static void main(String[] args) {
        int[] arrayPares = new int[6];
        
        arrayPares[0] = 2;
        arrayPares[1] = 4;
        arrayPares[2] = 6;
        arrayPares[3] = 8;
        arrayPares[4] = 10;
        arrayPares[5] = 12;

        for(int i=0; i<arrayPares.length; i++) {
            System.out.println(arrayPares[i]);
        }
    }
}